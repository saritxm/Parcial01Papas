    // Método para mostrar el panel de consulta por nombre.
    public void showNombre() {
        pNombre.setSize(680, 300);
        pNombre.setLocation(0, 0);
        // Elimina cualquier otro panel que esté actualmente mostrado
        PanelCambios.removeAll();
        // Agrega el panel de consulta por nombre al área de cambios
        PanelCambios.add(pNombre, new org.netbeans.lib.awtextra.AbsoluteConstraints(0, 0, -1, -1));
        // Revalida y repinta el área de cambios para mostrar el nuevo panel
        PanelCambios.revalidate();
        PanelCambios.repaint();
    }

